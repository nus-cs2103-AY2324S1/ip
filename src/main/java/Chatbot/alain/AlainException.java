package chatbot.alain;

/**
 * Custom exception class for handling specific exceptions in the Alain chatbot.
 */
public class AlainException extends Exception {

    /**
     * Constructs an AlainException with the given error message.
     *
     * @param message The error message associated with the exception.
     */
    public AlainException(String message) {
        super(message);
    }
}
