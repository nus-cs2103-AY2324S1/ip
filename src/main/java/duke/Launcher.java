package duke;

import javafx.application.Application;

/**
 * This class serves as the launcher for the Duke application.
 */
public class Launcher {
    /**
     * The main method of the Launcher class, which starts the Duke application.
     *
     * @param args Command-line arguments (if any) to pass to the application.
     */
    public static void main(String[] args) {
        Application.launch(Main.class, args);
    }
}
