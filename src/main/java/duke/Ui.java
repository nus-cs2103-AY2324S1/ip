package duke;

/**
 * Represents the User Interface (UI) of the application.
 * Contains methods for user interactions.
 */
public class Ui {

    /**
     * Prints the greeting message to the user.
     */
    public String sayhi() {
        return "Hello! I'm LimBay.\nWhat can I do for you?\n";
    }

    /**
     * Prints the goodbye message to the user.
     */
    public String saybye() {
        return "Bye. Hope to see you again soon!";
    }
}
