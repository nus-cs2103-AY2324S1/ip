package duke;

import duke.command.Command;
import duke.exception.DukeException;
import duke.parser.Parser;
import duke.storage.Storage;
import duke.task.TaskList;
import duke.ui.Ui;
import javafx.application.Platform;

/**
 * The main class that runs the Duke chatbot.
 */
public class Duke {
    private Storage storage;
    private TaskList tasks;
    private Ui ui;

    /**
     * Constructs a Duke instance with the specified file path for task storage.
     *
     * @param filePath The path to the file used for task storage.
     */
    public Duke(String filePath) {
        ui = new Ui();
        storage = new Storage(filePath);

        try {
            tasks = new TaskList(storage.load());
        } catch (DukeException e) {
            ui.showLoadingError();
            tasks = new TaskList();
        }
    }

    /**
     * Processes user input and generates a response.
     *
     * @param input The user input to be processed.
     * @return The response generated by processing the input.
     */
    public String getResponse(String input) {
        try {
            String fullCommand = input;
            Command c = Parser.parse(fullCommand);
            if (!c.isExit()) {
                c.execute(tasks, ui, storage);
                return ui.retrieveMessage();
            } else {
                Platform.exit();
                return ui.showBye();
            }
        } catch (DukeException e) {
            return ui.showError(e.getMessage());
        }
    }
}
